class Box {
    int value;
}

public class Main {

    public static int add(int a, int b) {
        return a + b;
    }

    public static long retLong() {
        return 100L;
    }

    public static Object retObj() {
        return new Box();
    }

    public static void retVoid() {
        return;
    }

    public static void main(String[] args) {

        // =================================================
        // ================= 常量 / 局部变量 =================
        // =================================================

        // bipush
        int a = 10;
        int b = 20;

        // sipush
        int big = 300;

        // =================================================
        // ================= if / else ======================
        // =================================================

        int c;
        if (a == b) {
            c = 1;
        } else {
            c = 2;
        }

        // dup
        int d = c + c;

        // pop（丢弃返回值）
        add(1, 2);

        if (d != big) {
            c = 3;
        }

        // =================================================
        // ================= while / for ===================
        // =================================================

        int i = 0;
        while (i < 3) {
            i = i + 1;
        }

        for (int j = 0; j < 2; j++) {
            c = c + 2;
        }

        // =================================================
        // ================= 多分支 if ======================
        // =================================================

        if (c == 5) {
            c = 100;
        } else if (c != 6) {
            c = 200;
        } else {
            c = 300;
        }

        // =================================================
        // ============== new / field ======================
        // =================================================

        Box box = new Box();           // new + invokespecial
        box.value = c;                // putfield
        int x = box.value;            // getfield
        c = x + 1;

        // =================================================
        // ================= 算术 / 位运算 ==================
        // =================================================

        int p = 7;
        int q = 3;

        int r1 = p + q;
        int r2 = p - q;
        int r3 = p * q;
        int r4 = p / q;
        int r5 = p % q;

        int m1 = p & q;
        int m2 = p | q;
        int m3 = p ^ q;
        int m4 = p << 1;
        int m5 = p >> 1;
        int m6 = p >>> 1;

        int inc = 0;
        inc++;
        inc += 2;

        // =================================================
        // ================= 单目 if =======================
        // =================================================

        int cond = -1;
        if (cond < 0) {
            cond = 10;
        }
        if (cond >= 0) {
            cond = 20;
        }

        // =================================================
        // ================= 三元运算符 =====================
        // =================================================

        int t = (a > b) ? 1 : 2;

        // =================================================
        // ================= 数组（int） ====================
        // =================================================

        int[] arr = new int[3];
        arr[0] = 10;
        arr[1] = 20;
        arr[2] = 30;

        int arrSum = arr[0] + arr[1] + arr[2];
        int len = arr.length;

        // =================================================
        // ================= 数组（引用） ===================
        // =================================================

        Box[] boxes = new Box[2];
        boxes[0] = new Box();
        boxes[1] = box;

        boxes[0].value = 111;
        int v0 = boxes[0].value;

        // =================================================
        // ================= switch ========================
        // =================================================

        int sw = 2;
        switch (sw) {
            case 1:
                c = 10;
                break;
            case 2:
                c = 20;
                break;
            case 3:
                c = 30;
                break;
            default:
                c = 40;
        }
        System.out.println(c);
    }
}
